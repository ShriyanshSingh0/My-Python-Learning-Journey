# Here are some examples of variables in Python:

name = "Alice"  # A string variable
age = 30        # An integer variable
height = 1.75   # A float variable
is_student = True  # A boolean variable

# You can also use variables in expressions:

greeting = "Hello, " + name + "!"  # Concatenating strings
age_next_year = age + 1  # Adding 1 to the age variable

# You can print the variables to see their values:

print(greeting)  # Output: Hello, Alice!
print("Age next year:", age_next_year)  # Output: Age next year: 31

# You can also change the value of a variable:

name = "Bob"  # Now the name variable holds a different value
greeting = "Hello, " + name + "!"  # Updating the greeting with the new name
print(greeting)  # Output: Hello, Bob!